void graphics_draw_titlescreen(WINDOW *w,int x, int y){

	/* Title Screen Graphic */
		wattron(w,A_ALTCHARSET); 
		for(int i = 0; i < 12; ++i){
			for(int j = 0; j < 61; ++j){
				if(GRAPHICS_TITLE[i][j] == 'M'){
					wattron(w,A_REVERSE); 
					mvwaddch(w,y+i,x+j,' ');
					wattroff(w,A_REVERSE); 
				}	
				else if(GRAPHICS_TITLE[i][j] == 'm'){
					wattron(w,A_REVERSE); 
					mvwaddch(w,y+i,x+j,ACS_CKBOARD);
					wattroff(w,A_REVERSE); 
				}
				else if(GRAPHICS_TITLE[i][j] == 'n'){
					mvwaddch(w,y+i,x+j,ACS_CKBOARD);
				}
			}
		}
		wattroff(w,A_ALTCHARSET); 
		mvwprintw(w, y+20, x+7, "Use arrow keys to navigate and spacebar to select"); 

}
